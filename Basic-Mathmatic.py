
# Valores a serem inseridos nos cálculos

valor1 = input("Digite o primeiro valor: ")
valor2 = input("Digite o segundo valor: ")

#Operações com as variáveis valor1 e valor2

soma = int(valor1) + int(valor2)
subtracao = int(valor1) - int(valor2)
multiplicacao = int(valor1) * int(valor2)
divisao = int(valor1) / int(valor2)

#Mostrando o resultado das operações

print("A soma é: {}".format(soma))
print("A subtração é: {}".format(subtracao))
print("A multiplicacao é: {}".format(multiplicacao))
print("A visisao é: {}".format(divisao))